package com.aplana.sbrf.taxaccounting.scheduler.api.form;

import java.io.Serializable;

/**
 * Модель значения который используется как значение для
 * выпадающего списка
 */
public class SelectBoxItem implements Serializable {
    private String label;
    private Integer id;

    public SelectBoxItem() {
    }

    public SelectBoxItem(String label, Integer id) {
        this.label = label;
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        SelectBoxItem that = (SelectBoxItem) o;

        if (id != null ? !id.equals(that.id) : that.id != null) return false;
        if (label != null ? !label.equals(that.label) : that.label != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = label != null ? label.hashCode() : 0;
        result = 31 * result + (id != null ? id.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return label;
    }
}
